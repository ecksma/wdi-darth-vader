class Person
  def initialize(name, catchphrase, age, eyecolor, hair)
    @name = name
    @catchphrase = catchphrase
    @age = age
    @eyecolor = eyecolor
    @hair = hair
  end

  def name
    @name
  end

  def catchphrase
    @name
  end

  def age
    @name
  end

  def eyecolor
    @name
  end

  def hair
    @name
  end

  def to_s
    puts "My name is #{@name} and i like to say #{@catchphrase} and I am #{@age} with #{@eyecolor} eyes and #{@hair} hair"
  end
end
