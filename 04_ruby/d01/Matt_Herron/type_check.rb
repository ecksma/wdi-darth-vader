## Repeat for...

puts '"Bob" is a ' + "Bob".class.to_s
puts '34 is a ' + 34.class.to_s
puts '3.435 is a ' + 3.435.class.to_s
puts '[3, 5, 7, 2] is a ' + [3, 5, 7, 2].class.to_s
puts ':john is a ' + :john.class.to_s
puts 'true is a ' + true.class.to_s
puts 'false is a ' + false.class.to_s
puts '{:key => "value"} is a ' + {:key => "value"}.class.to_s
