var thingsThatMatter = ["Sara", "Rooney", "Family", "Friends", "health", "wealth", "fitness", "hobbies", "food"];


// For-In Loop
for (var things in thingsThatMatter) {
  console.log(thingsThatMatter[things]);
}

// For Loop
for (inc = 0; inc < thingsThatMatter.length; inc++) {
  console.log(thingsThatMatter[inc]);
}
